// Countdown Timer using a for loop
// Loop for decrementation from 10 to 1
for (let i = 10; i >= 1; i--) {
    console.log(i);
}

console.log("Lift off!");

/*
DESKTOP TEST 

Initial state:
let i = 10     → starting point of the countdown

i >= 1         → loop continues as long as i is greater than or equal to 1

i--            → decreases i by 1 after each loop iteration (decrement)

console.log(i) → prints the current value of i at each step

console.log("Lift off!")
    → runs after the loop ends, signaling completion
*/